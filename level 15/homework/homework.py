# მომხმარებლისგან რიცხვის მიღება
user_input = input("შეიყვანეთ რიცხვი 1-დან 1000-მდე: ")

# სცადეთ შევცვალოთ რიცხვი, რათა გადავამოწმოთ მისი ვალიდურობა
try:
    number = int(user_input)
    
    # შეამოწმეთ რიცხვის დიაპაზონი
    if 1 <= number <= 1000:
        print("სწორია!")
    else:
        print("არასწორია!")
except ValueError:
    print("არასწორია!")
