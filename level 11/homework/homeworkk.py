# პროგრამის დასაწყისი
def main():
    # პირველ პირზე ასაკის მიღება
    age1 = int(input("შეიყვანეთ პირველი პირის ასაკი: "))
    
    # მეორე პირზე ასაკის მიღება
    age2 = int(input("შეიყვანეთ მეორე პირის ასაკი: "))
    
    # ასაკების შედარება
    if age1 > age2:
        print("პირველი პირი უფროსია.")
    elif age1 < age2:
        print("მეორე პირი უფროსია.")
    else:
        print("ორივე პირი იმავე ასაკისაა.")
    
# მთავარი ფუნქციის გაწვდა
if __name__ == "__main__":
    main()
